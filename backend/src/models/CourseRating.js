import mongoose from "mongoose";

const courseRatingSchema = new mongoose.Schema(
  {
    courseId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Course",
      required: true,
      index: true,
    },
    studentId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
      index: true,
    },
    rating: {
      type: Number,
      required: true,
      min: 1,
      max: 5,
    },
  },
  {
    timestamps: true,
  }
);

courseRatingSchema.index({ courseId: 1, studentId: 1 }, { unique: true });

export default mongoose.model("CourseRating", courseRatingSchema);
